#include <lvgl.h>

#include <TFT_eSPI.h>

#define TFT_HOR_RES   240
#define TFT_VER_RES   240

#define DRAW_BUF_SIZE (TFT_HOR_RES * TFT_VER_RES / 10 * (LV_COLOR_DEPTH / 8))
uint32_t draw_buf[DRAW_BUF_SIZE / 4];


